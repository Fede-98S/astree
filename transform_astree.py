def fix_links(root):
    # Fix persName ref
    for elem in root.xpath("//tei:persName[@ref]", namespaces={'tei': TEI_NS}):
        ref = elem.get("ref")
        # personnage_aa.html#2_astree -> personnages.html#2_astree
        # OR personnage_aa.html#astree -> personnages.html#astree
        m = re.search(r'personnage_[a-z]+\.html#(?:(\d+)_)?([a-z_0-9]+)', ref)
        if m:
            prefix = f"{m.group(1)}_" if m.group(1) else ""
            elem.set("ref", f"personnages.html#{prefix}{m.group(2)}")
        else:
            m = re.search(r'personnages\.html#([a-z_0-9]+)_(\d+)', ref)
            if m:
                elem.set("ref", f"personnages.html#{m.group(2)}_{m.group(1)}")

    # Fix term ref (glossary)
    for elem in root.xpath("//tei:term[@ref]", namespaces={'tei': TEI_NS}):
        ref = elem.get("ref")
        m = re.search(r'glossaire_[a-z]+\.html#([a-z_0-9]+)', ref)
        if m:
            elem.set("ref", f"glossaire.html#{m.group(1)}")

    # Fix rs ref (repertoire)
    for elem in root.xpath("//tei:rs[@ref]", namespaces={'tei': TEI_NS}):
        ref = elem.get("ref")
        m = re.search(r'repertoire_[a-z]+\.html#(?:(\d+)_)?([a-z_0-9]+)', ref)
        if m:
            prefix = f"{m.group(1)}_" if m.group(1) else ""
            elem.set("ref", f"repertoire.html#{prefix}{m.group(2)}")

    # Fix note target
    for elem in root.xpath("//tei:note[@target]", namespaces={'tei': TEI_NS}):
        target = elem.get("target")
        m = re.search(r'note_(\d+)\.html#(?:\d+_)?([a-z_0-9]+)', target)
        if m:
            elem.set("target", f"notes_{m.group(1)}.html#{m.group(2)}")

def clean_tei_roman(tree, is_functional=False):
    # Remove navigation (any p that looks like a nav or has multiple refs)
    for p in tree.xpath("//tei:p[tei:ref and count(tei:ref) > 1]", namespaces={'tei': TEI_NS}):
        p.getparent().remove(p)
    
    if is_functional:
        body = tree.xpath("//tei:body", namespaces={'tei': TEI_NS})[0]
        
        # Remove everything before the first div that starts with a head (usually the book/part title)
        # OR the first p that contains a pb/graphic lettrine.
        # But looking at livre2_1_f.xml model, it has <head> elements for title first.
        
        children = list(body)
        keep_from_index = -1
        
        # Look for the real content start
        for i, child in enumerate(children):
            # The real content usually starts with a <div> containing a <head> 
            # OR a <head> directly in body (like livre2_1_f.xml model)
            if child.tag == f"{{{TEI_NS}}}head" or child.tag == f"{{{TEI_NS}}}div":
                # Check if it's not a nav div (one that contains many refs)
                if not child.xpath(".//tei:ref[contains(@target, '_analyse/')]", namespaces={'tei': TEI_NS}):
                    keep_from_index = i
                    break
            # Or it might start with a <p> containing a <pb> or a lettrine <graphic>
            if child.tag == f"{{{TEI_NS}}}p" and (child.xpath(".//tei:pb", namespaces={'tei': TEI_NS}) or child.xpath(".//tei:graphic[contains(@url, 'page1')]", namespaces={'tei': TEI_NS})):
                keep_from_index = i
                break

        if keep_from_index != -1:
            # Remove everything before the identified start index
            for child in children[:keep_from_index]:
                body.remove(child)
        
        # Also remove everything after the main content
        # Usually trails off into Référence électronique or Google Analytics
        # In the model livre2_1_f.xml, it ends after the body.
        # Let's find common footer elements and remove them and everything after.
        children_after = list(body)
        remove_from_index = -1
        for i, child in enumerate(children_after):
            if (child.tag == f"{{{TEI_NS}}}p" and child.get("rend") == "center_t titre9" and "Référence électronique" in "".join(child.itertext())) or \
               (child.tag == f"{{{TEI_NS}}}ref" and "statcounter.com" in (child.get("target") or "")):
                remove_from_index = i
                break
        
        if remove_from_index != -1:
            for child in children_after[remove_from_index:]:
                body.remove(child)
        
        # Final cleanup: remove residual empty paragraphs or line breaks at the very beginning of body
        for child in list(body):
            if child.tag == f"{{{TEI_NS}}}lb" or (child.tag == f"{{{TEI_NS}}}p" and not child.text and not list(child)):
                body.remove(child)
            else:
                break

    # Remove signet graphics and their hi containers
    for graphic in tree.xpath("//tei:graphic[contains(@url, 'signet')]", namespaces={'tei': TEI_NS}):
        parent = graphic.getparent()
        if parent is not None:
            if parent.tag in [f"{{{TEI_NS}}}hi", f"{{{TEI_NS}}}p"]:
                gp = parent.getparent()
                if gp is not None: gp.remove(parent)
            else:
                parent.remove(graphic)
                
    # Update image paths (remove ../../../images/ -> images/)
    for graphic in tree.xpath("//tei:graphic[@url]", namespaces={'tei': TEI_NS}):
        url = graphic.get("url")
        if "../../../images/" in url:
            graphic.set("url", url.replace("../../../images/", "images/"))

    fix_links(tree)

def apply_xslt(xml_path, xsl_path, output_html_path):
    print(f"Applying {xsl_path} to {xml_path}...")
    xml_tree = etree.parse(xml_path)
    xslt_tree = etree.parse(xsl_path)
    transform = etree.XSLT(xslt_tree)
    result_tree = transform(xml_tree)
    with open(output_html_path, "wb") as f_out:
        f_out.write(etree.tostring(result_tree, pretty_print=True, method="html", encoding="UTF-8"))

def transform_roman():
    print("Transforming Roman files...")
    # Process reference files
    ref_files = glob.glob(os.path.join(ROMAN_DIR, "reference", "*.xml"))
    for f in ref_files:
        parser = etree.XMLParser(remove_blank_text=True)
        tree = etree.parse(f, parser)
        clean_tei_roman(tree, is_functional=False)
        
        # Output filename: 21_2_livre_1.tei.xml -> livre2_1.xml
        basename = os.path.basename(f)
        m = re.search(r'21_(\d)_livre_(\d+)', basename)
        if m:
            out_xml = f"livre{m.group(1)}_{m.group(2)}.xml"
            output_path = os.path.join(OUTPUT_DIR, out_xml)
            with open(output_path, "wb") as f_out:
                f_out.write(etree.tostring(tree, pretty_print=True, xml_declaration=True, encoding="UTF-8"))
            # Apply XSLT
            apply_xslt(output_path, XSLT_TRANSFORMATION, output_path.replace(".xml", ".html"))

    # Process fonctionnelle files
    func_files = glob.glob(os.path.join(ROMAN_DIR, "fonctionnelle", "*.xml"))
    for f in func_files:
        parser = etree.XMLParser(remove_blank_text=True)
        tree = etree.parse(f, parser)
        clean_tei_roman(tree, is_functional=True)
        
        # Output filename: fonctionnelle_2_livre_1.xml -> livre2_1_f.xml
        basename = os.path.basename(f)
        m = re.search(r'fonctionnelle_(\d)_livre_(\d+)', basename)
        if m:
            out_xml = f"livre{m.group(1)}_{m.group(2)}_f.xml"
            output_path = os.path.join(OUTPUT_DIR, out_xml)
            with open(output_path, "wb") as f_out:
                f_out.write(etree.tostring(tree, pretty_print=True, xml_declaration=True, encoding="UTF-8"))
            # Apply XSLT
            apply_xslt(output_path, XSLT_TRANSFORMATION, output_path.replace(".xml", ".html"))

def main():
    ensure_dir(OUTPUT_DIR)
    
    # 1. Apparatus
    transform_glossary()
    transform_note_grp("notes", "Notes", "notes_2.xml", is_already_notes=True)
    transform_note_grp("personnages", "Personnages", "personnages.xml")
    transform_note_grp("repertoire", "Répertoire", "repertoire.xml")
    
    # 2. Roman
    transform_roman()
    
    # 3. Final Apparatus HTML (using their specific XSLTs)
    apply_xslt(os.path.join(OUTPUT_DIR, "glossaire.xml"), XSLT_APPARAT, os.path.join(OUTPUT_DIR, "glossaire.html"))
    apply_xslt(os.path.join(OUTPUT_DIR, "notes_2.xml"), XSLT_APPARAT, os.path.join(OUTPUT_DIR, "notes_2.html"))
    apply_xslt(os.path.join(OUTPUT_DIR, "personnages.xml"), XSLT_REPERTOIRE, os.path.join(OUTPUT_DIR, "personnages.html"))
    apply_xslt(os.path.join(OUTPUT_DIR, "repertoire.xml"), XSLT_REPERTOIRE, os.path.join(OUTPUT_DIR, "repertoire.html"))

    # 4. Generate Index
    generate_index_html()

    print("Done!")

if __name__ == "__main__":
    main()
