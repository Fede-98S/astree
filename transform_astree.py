import os
import re
import glob
import copy
from lxml import etree

# Configuration
SOURCE_DIR = "xml_depart"
OUTPUT_DIR = "transformation_automatique"
ANALYSE_DIR = os.path.join(SOURCE_DIR, "analyse")
ROMAN_DIR = os.path.join(SOURCE_DIR, "roman")

# XSLT files
XSLT_TRANSFORMATION = "transformation.xsl"
XSLT_APPARAT = "apparat.xsl"
XSLT_REPERTOIRE = "repertoire.xsl"

# TEI Namespace
TEI_NS = "http://www.tei-c.org/ns/1.0"
HTML_NS = "http://www.w3.org/1999/xhtml"
NS_MAP = {None: TEI_NS, 'html': HTML_NS}

def ensure_dir(directory):
    if not os.path.exists(directory):
        os.makedirs(directory)

def clean_text(text):
    if text is None:
        return ""
    return re.sub(r'[\n\t\s]+', ' ', text).strip()

def create_tei_base(title):
    root = etree.Element(f"{{{TEI_NS}}}TEI", nsmap=NS_MAP)
    header = etree.SubElement(root, f"{{{TEI_NS}}}teiHeader")
    fileDesc = etree.SubElement(header, f"{{{TEI_NS}}}fileDesc")
    titleStmt = etree.SubElement(fileDesc, f"{{{TEI_NS}}}titleStmt")
    t = etree.SubElement(titleStmt, f"{{{TEI_NS}}}title")
    t.text = title
    etree.SubElement(fileDesc, f"{{{TEI_NS}}}publicationStmt")
    sourceDesc = etree.SubElement(fileDesc, f"{{{TEI_NS}}}sourceDesc")
    p = etree.SubElement(sourceDesc, f"{{{TEI_NS}}}p")
    p.text = "Generated by automated transformation script"
    text_elem = etree.SubElement(root, f"{{{TEI_NS}}}text")
    body = etree.SubElement(text_elem, f"{{{TEI_NS}}}body")
    div = etree.SubElement(body, f"{{{TEI_NS}}}div")
    return root, div

def transform_glossary():
    print("Transforming Glossary...")
    files = glob.glob(os.path.join(ANALYSE_DIR, "glossaire", "glossaire_*.xml"))
    root, div = create_tei_base("Glossaire")
    
    files.sort()
    used_ids = set()
    
    for f in files:
        parser = etree.XMLParser(remove_blank_text=True)
        tree = etree.parse(f, parser)
        
        tables = tree.xpath("//tei:table[not(@xml:id='draggable')]", namespaces={'tei': TEI_NS})
        for table in tables:
            rows = table.xpath("./tei:row", namespaces={'tei': TEI_NS})
            for row in rows:
                cells = row.xpath("./tei:cell", namespaces={'tei': TEI_NS})
                if len(cells) >= 2:
                    term_cell = cells[0]
                    def_cell = cells[1]
                    
                    term_id = term_cell.get("{http://www.w3.org/XML/1998/namespace}id")
                    term_text = "".join(term_cell.itertext()).strip()
                    
                    if not term_text or term_text == "Terme": continue

                    entry = etree.SubElement(div, f"{{{TEI_NS}}}entry")
                    
                    base_id = term_id if term_id else term_text.lower().replace(" ", "_").replace("'", "_")
                    base_id = re.sub(r'[^a-zA-Z0-9_.-]', '_', base_id)
                    unique_id = base_id
                    counter = 1
                    while unique_id in used_ids:
                        unique_id = f"{base_id}_{counter}"
                        counter += 1
                    used_ids.add(unique_id)
                    entry.set("{http://www.w3.org/XML/1998/namespace}id", unique_id)

                    form = etree.SubElement(entry, f"{{{TEI_NS}}}form")
                    form.text = term_text
                    
                    definition = etree.SubElement(entry, f"{{{TEI_NS}}}def")
                    for child in def_cell:
                        definition.append(copy.deepcopy(child))
                    if not list(definition) and def_cell.text:
                        definition.text = def_cell.text

    output_path = os.path.join(OUTPUT_DIR, "glossaire.xml")
    with open(output_path, "wb") as f_out:
        f_out.write(etree.tostring(root, pretty_print=True, xml_declaration=True, encoding="UTF-8"))

def transform_note_grp(subdir, title, output_filename, is_already_notes=False):
    print(f"Transforming {title}...")
    pattern = "note_*.xml" if subdir == "notes" else f"{subdir[:-1] if subdir != 'repertoire' else 'repertoire'}_*.xml"
    if subdir == "repertoire": pattern = "repertoire_*.xml"
    if subdir == "personnages": pattern = "personnage_*.xml"
    
    files = glob.glob(os.path.join(ANALYSE_DIR, subdir, pattern))
    root, div = create_tei_base(title)
    noteGrp = etree.SubElement(div, f"{{{TEI_NS}}}noteGrp")
    
    files.sort()
    used_ids = set()
    
    for f in files:
        parser = etree.XMLParser(remove_blank_text=True)
        tree = etree.parse(f, parser)
        
        if is_already_notes:
            notes = tree.xpath("//tei:note", namespaces={'tei': TEI_NS})
            ana_match = re.search(r'note_(\d+)', os.path.basename(f))
            ana = ana_match.group(1) if ana_match else ""
            for note_src in notes:
                note = copy.deepcopy(note_src)
                if ana:
                    note.set("ana", ana)
                
                orig_id = note.get("{http://www.w3.org/XML/1998/namespace}id")
                if orig_id:
                    unique_id = orig_id
                    counter = 1
                    while unique_id in used_ids:
                        unique_id = f"{orig_id}_{counter}"
                        counter += 1
                    used_ids.add(unique_id)
                    note.set("{http://www.w3.org/XML/1998/namespace}id", unique_id)
                
                noteGrp.append(note)
        else:
            tables = tree.xpath("//tei:table[not(@xml:id='draggable')]", namespaces={'tei': TEI_NS})
            for table in tables:
                rows = table.xpath("./tei:row", namespaces={'tei': TEI_NS})
                for row in rows:
                    cells = row.xpath("./tei:cell", namespaces={'tei': TEI_NS})
                    if len(cells) >= 1:
                        note = etree.SubElement(noteGrp, f"{{{TEI_NS}}}note")
                        
                        label_text = "".join(cells[0].itertext()).strip()
                        if not label_text or label_text in ["Nom", "Personnage"]: continue
                        
                        label_text = re.sub(r'[:η\s]+$', '', label_text)
                        label = etree.SubElement(note, f"{{{TEI_NS}}}label")
                        label.text = label_text
                        
                        orig_id = cells[0].get("{http://www.w3.org/XML/1998/namespace}id")
                        base_id = orig_id if orig_id else label_text.lower().replace(" ", "_").replace("'", "_")
                        base_id = re.sub(r'[^a-zA-Z0-9_.-]', '_', base_id)
                        
                        unique_id = base_id
                        counter = 1
                        while unique_id in used_ids:
                            unique_id = f"{base_id}_{counter}"
                            counter += 1
                        used_ids.add(unique_id)
                        note.set("{http://www.w3.org/XML/1998/namespace}id", unique_id)

                        for i in range(1, len(cells)):
                            for child in cells[i]:
                                note.append(copy.deepcopy(child))
                            if not list(cells[i]) and cells[i].text:
                                p = etree.SubElement(note, f"{{{TEI_NS}}}p")
                                p.text = cells[i].text

    output_path = os.path.join(OUTPUT_DIR, output_filename)
    with open(output_path, "wb") as f_out:
        f_out.write(etree.tostring(root, pretty_print=True, xml_declaration=True, encoding="UTF-8"))

def main():
    ensure_dir(OUTPUT_DIR)
    
    # 1. Apparatus
    transform_glossary()
    transform_note_grp("notes", "Notes", "notes_2.xml")
    transform_note_grp("personnages", "Personnages", "personnages.xml")
    transform_note_grp("repertoire", "Répertoire", "repertoire.xml")
    
